    //fungsi untuk cek data sudah terisi semua apa belum
    public boolean validateText(){
        if (rrID.getText().isEmpty() || rrReqDate.getDate().equals(null) || rrType.getText().isEmpty() || rrNum.getText().isEmpty() || rrDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tolong Diisi Semua!", "Failed Login", 2);
            return false;
        }else{
            return true;
        }
    }
    
    //fungsi untuk menyimpan data yang ada pada arraylist ke dalam file.dat
    public void savetoFile(){
        
        try{
            
            FileOutputStream file = new FileOutputStream("resource.dat");
            ObjectOutputStream outputfile = new ObjectOutputStream(file);
            
            for (int i = 0; i < r.size(); i++){
                outputfile.writeObject(r.get(i));
            }
            
            outputfile.close();
            
            JOptionPane.showMessageDialog(null, "Successfully Saved!!");
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    //fungsi untuk menyimpan data ke dalam array list dari file.dat
    public void regisArrayList(){
        try{
            FileInputStream file = new FileInputStream("resource.dat");
            ObjectInputStream inputfile = new ObjectInputStream(file);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    r.add((Resource) inputfile.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

