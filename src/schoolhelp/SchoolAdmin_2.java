    //fungsi untuk mengjukan reqeust
    private void AjukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjukanActionPerformed
        // cek data sudah terisi semua atau belum
        if (validateText()){
            //fungsi untuk memformat tanggal dan cash ke string
            SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");

            String reqID = rrID.getText().trim();
            String Status = "PENDING";
            String ReqDate = dt.format(rrReqDate.getDate());
            String Type = rrType.getText().trim();
            String Num = rrNum.getText().trim();
            String Desc = rrDesc.getText().trim();
            String SchoolName = SchoolNameAdmin.getText();
            
            //panggil contructor dan simpan data
            Resource R = new Resource(SchoolName, Type, Num, reqID, ReqDate, Status, Desc);
            
            //simpan object resource ke dalam arraylist
            r.add(R);
            
            //fungsi untuk menyimpan object ke file.dat
            savetoFile();
        }
    }//GEN-LAST:event_AjukanActionPerformed

