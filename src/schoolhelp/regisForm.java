    //Fungsi ini terjadi jika kita mengklik tombol regis
    private void RegisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisButtonActionPerformed
        // Mengecek apakah sudah terisi semua data yang ada
        
        if(validateText()){
            //ambil semua data yang telah kita isikan disimpan kedalam variable baru
            String user = vUser.getText().trim();
            String Pass = vPass.getText().trim();
            String Name = vName.getText().trim();
            String email = vEmail.getText().trim();
            String Telepon = vTelepon.getText().trim();
            String Job = vJob.getText().trim();
            
            //ini berguna untuk memformat JDateChooser (Tanggal) menjadi tahun-bulan-hari
            //Lalu diubah menjadi String
            SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
            String Date = dt.format(vDate.getDate());
            
            //simpan kedalam varibael cek dari fungsi cekUsername
            boolean cek = cekUsername(user);
            
            //apabila mengembalikan nilai true maka username yang kita daftarkan harus diganti 
            //sebab telah ada yang menggunakannya
            if (cek){
                JOptionPane.showMessageDialog(null, Name + " Telah Digunakan, Silahkan Coba yang lain!");
            }else{
                //jika tidak maka panggil contructor dari class Volunter dan simpan semua variabel
                Volunteer vt = new Volunteer(Date, Job, user, Pass, Name, email, Telepon);
                
                //lalu add ke dalam array list
                v.add(vt);
                
                //dan simpan kedalam file.dat
                savetoFile();
            }
        }
        
    }//GEN-LAST:event_RegisButtonActionPerformed

