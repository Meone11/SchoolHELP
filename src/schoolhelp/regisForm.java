    //Fungsi ini untuk mengecek apakah Masih Ada JTExtField yang kosong apa tidak
    public boolean validateText(){
        if (vUser.getText().isEmpty() || vPass.getText().isEmpty() || vName.getText().isEmpty() ||
                vEmail.getText().isEmpty() || vTelepon.getText().isEmpty() || vJob.getText().isEmpty() || vDate.getDate().equals(null)){
            JOptionPane.showMessageDialog(null, "Tolong Diisi Semua!", "Failed Login", 2);
            return false;
        }else if ( vDate.getDate().compareTo(new Date()) > 0){
            JOptionPane.showMessageDialog(null, "Tolong isi Tanggal lahir dengan Benar");
            return false;
        }else{
            return true;
        }
    }
    
    //fungsi ini berguna untuk menyimpan data yang telah kita input pada arraylist ke dalam file.data
    public void savetoFile(){
        
        try{
            
            FileOutputStream file = new FileOutputStream("regis.dat");
            ObjectOutputStream outputfile = new ObjectOutputStream(file);
            
            for (int i = 0; i < v.size(); i++){
                outputfile.writeObject(v.get(i));
            }
            
            outputfile.close();
            
            JOptionPane.showMessageDialog(null, "Successfully Register!!");
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }   
    }
    
    //Fungsi ini berguna untuk membaca isi dari file.data dan didsimpan didalam arrayList
    public void regisArrayList(){
        try{
            FileInputStream file = new FileInputStream("regis.dat");
            ObjectInputStream inputfile = new ObjectInputStream(file);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    v.add((Volunteer) inputfile.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }        
    }
    
    //Fungsi ini berguna agar username yang kita daftarkan tidak terdaftar 2x
    public boolean cekUsername(String Username){
        boolean cek = false;
        try{
            
            for (int i = 0; i < v.size(); i++){
                if ( Username.equals(v.get(i).getUsername()) ){
                    cek = true;
                    break;
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return cek;
    }

