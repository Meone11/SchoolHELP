    //fungsi untuk ngecek apakah data reuqest sudah terisi semua atau belum
    public boolean validateText(){
        if (trID.getText().isEmpty() || trReqDate.getDate().equals(null) || trPDate.getDate().equals(null)
                || trTime.getText().isEmpty() || trLevel.getText().isEmpty() || trSum.getText().isEmpty() || trDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tolong Diisi Semua!", "Failed Login", 2);
            return false;
        }else{
            return true;
        }
    }
    
    //fungsi untuk menyimpan data yang ada didalam arrayList ke dalam file.dat
    public void savetoFile(){
        
        try{
            
            FileOutputStream file = new FileOutputStream("tutorial.dat");
            ObjectOutputStream outputfile = new ObjectOutputStream(file);
            
            for (int i = 0; i < t.size(); i++){
                outputfile.writeObject(t.get(i));
            }
            
            outputfile.close();
            
            JOptionPane.showMessageDialog(null, "Successfully Saved!!");
            
        }catch(IOException e){
//            JOptionPane.showMessageDialog(null, "Salah Disini!");
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    //fungsi untuk membaca data dari file.dat dan akan disimpan kedalam arraylist
    public void regisArrayList(){
        try{
            FileInputStream file = new FileInputStream("tutorial.dat");
            ObjectInputStream inputfile = new ObjectInputStream(file);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    t.add((Tutorial) inputfile.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

