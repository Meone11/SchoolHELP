    //fungsi untuk mengajukan request
    private void AjukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjukanActionPerformed
        // cek apakah data sudah terisi semua atau belum
        if (validateText()){
            //fungsi untuk memformat tanggal dan ngecash ke string
            SimpleDateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
            String reqID = trID.getText().trim();
            String Status = "PENDING";
            String reqDate = dt.format(trReqDate.getDate());
            String PropDate = dt.format(trPDate.getDate());
            String Time = trTime.getText().trim();
            String Level = trLevel.getText().trim();
            String Sum = trSum.getText().trim();
            String Desc = trDesc.getText().trim();
            String Schoolname = SchoolNameAdmin.getText().trim();
            
            //panggil contructor tutorial
            Tutorial T = new Tutorial(Schoolname, PropDate, Time, Level, Sum, reqID, reqDate, Status , Desc);
            
            //data dari constructor disimpan ke dalam arraylist
            t.add(T);
            
            //data dari array list akan disimpan ke dalam file.dat
            savetoFile();
        }
        
    }//GEN-LAST:event_AjukanActionPerformed

